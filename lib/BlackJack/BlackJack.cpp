const unsigned char* blackJackApp_allFrames[1] = {
  blackJackApp_frame_0_bits // Frame 1
};

const int blackJackApp_frameCount = 1;

const int blackJackApp_frameSpeed = 100;


BlackJackApp::BlackJackApp(ButtonManager& btnMgr) : 
    buttonManager(btnMgr), 
    display(HAL::displayProxy()), 
    lastFrameTime(0), 
    currentFrame(0) {
    instance = this;
}

void BlackJackApp::begin() {
    buttonManager.registerCallback(button_BottomLeftIndex, onButtonBackPressed);
    lastFrameTime = 0;
    currentFrame = 0;
}

void BlackJackApp::end() {
    buttonManager.unregisterCallback(button_BottomLeftIndex);
}

void BlackJackApp::update() {
    unsigned long now = millis();
    if (blackJackApp_frameCount <= 0) return; // No frames

    // Handle static image (1 frame)
    if (blackJackApp_frameCount == 1) {
        if (lastFrameTime == 0) { // Draw only once
            display.clear();
            if (blackJackApp_allFrames[0] != NULL) {
                display.drawXbm(0, 0, 128, 64, blackJackApp_allFrames[0]);
            } else {
                display.drawString(0, 0, "Bitmap Err!");
            }
            display.display();
            lastFrameTime = 1; // Set flag to indicate "drawn"
        }
        return;
    }

    // Handle animation
    if (lastFrameTime != 0 && (now - lastFrameTime < blackJackApp_frameSpeed)) {
        return; // Not time for next frame
    }
    
    lastFrameTime = now;
    display.clear();

    if (currentFrame < 0 || currentFrame >= blackJackApp_frameCount) {
        currentFrame = 0;
    }

    if (blackJackApp_allFrames[currentFrame] != NULL) {
        display.drawXbm(0, 0, 128, 64, blackJackApp_allFrames[currentFrame]);
    } else {
        display.drawString(0, 0, "Frame Err!");
        // ESP_LOGE(TAG_MAIN, "[BlackJackApp] Bitmap error at frame %d", currentFrame);
    }
    
    display.display();
    currentFrame = (currentFrame + 1) % blackJackApp_frameCount;
}

void BlackJackApp::onButtonBackPressed(const ButtonEvent& e) {
    if (e.eventType == ButtonEvent_Released) {
        if (instance) {
            // ESP_LOGI(TAG_MAIN, "[BlackJackApp] Back button pressed");
            instance->end(); // Unregister button
            MenuManager::instance().returnToMenu();
        }
        /* else {
            ESP_LOGE(TAG_MAIN, "[BlackJackApp] Back button pressed, but instance is null!");
        } */
    }
}